#-----------------Conversão de Temperaturas---------------------#
#conversão de temperatudo 
C = float(input('coloque a temperatura aqui °C: '))
F = ((9 * C) / 5) + 32#operação realizada para se obter o valor de conversão da temperatura recomendado colocar parenteses na operaçao da variavel C para prevalecer a hierarquia das operações 
print('a temperatura em {}°C convertida em farenheit sera:{}°F!'.format(C, F));#impressão dos valores convertidos da temperatuda 
